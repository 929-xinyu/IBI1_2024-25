# 步行 + 公交的耗时（单位：分钟）
a = 15       # 步行到公交站
b = 75       # 公交行程（1小时15分钟 = 75分钟）
c = a + b    # 总时间

# 开车 + 步行的耗时
d = 90       # 车程（1小时30分钟 = 90分钟）
e = 5        # 停车场步行到办公室
f = d + e    # 总时间

# 比较两种通勤方式的总时间
if c < f:
    print("步行+公交更快，总耗时：", c, "分钟")
else:
    print("开车+步行更快，总耗时：", f, "分钟")
